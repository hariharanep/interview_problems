package com.smic.practice.sort;

public class Pair {
    int low ;
    int high ;

    Pair(int l, int h) {
        this.low  = l ;
        this.high = h ;
    }

    public int getLow() {
        return low;
    }

    public void setLow(int low) {
        this.low = low;
    }

    public int getHigh() {
        return high;
    }

    public void setHigh(int high) {
        this.high = high;
    }
}
